// Macro add_these_plz with args ['a', 'b'] expanding to 'a + b'

bool isEven(int   x) {
    return x % 2;
}

int main() {
    cout << "whoa, let's add some things" << endl;
    cout << 1 + 2 << endl;
    cout << "I wonder if they're even?" << endl;
    cout << 1 + 2 + 3 << endl;
    cout << isEven(1 + 2) << endl;
}
